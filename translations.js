// ==================== LANGUAGE SWITCHING ====================
window.currentLang = localStorage.getItem('jtd-lang') || DEFAULT_LANGUAGE;

function setLanguage(lang) {
    if (SUPPORTED_LANGUAGES.indexOf(lang) === -1) {
        lang = DEFAULT_LANGUAGE;
    }

    window.currentLang = lang;
    localStorage.setItem('jtd-lang', lang);
    document.documentElement.setAttribute('data-lang', lang);

    // Set text direction for RTL languages (Urdu)
    document.documentElement.setAttribute('dir', lang === 'ur' ? 'rtl' : 'ltr');

    // Update all translatable elements (plain text)
    document.querySelectorAll('[data-i18n]').forEach(function (el) {
        var key = el.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            el.textContent = translations[lang][key];
        } else if (translations.en[key]) {
            el.textContent = translations.en[key];
        }
    });

    // Update all translatable elements (HTML content)
    document.querySelectorAll('[data-i18n-html]').forEach(function (el) {
        var key = el.getAttribute('data-i18n-html');
        if (translations[lang] && translations[lang][key]) {
            el.innerHTML = translations[lang][key];
        } else if (translations.en[key]) {
            el.innerHTML = translations.en[key];
        }
    });

    // Update language toggle buttons
    document.querySelectorAll('.lang-btn').forEach(function (btn) {
        btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
    });

    // Update page title
    var path = window.location.pathname;
    var page = 'index';
    if (path.indexOf('about') !== -1) page = 'about';
    else if (path.indexOf('contact') !== -1) page = 'contact';
    else if (path.indexOf('register') !== -1) page = 'register';
    else if (path.indexOf('resources') !== -1) page = 'resources';
    else if (path.indexOf('blog') !== -1) page = 'blog';
    else if (path.indexOf('compassion-course') !== -1) page = 'compassion-course';

    if (pageTitles[lang] && pageTitles[lang][page]) {
        document.title = pageTitles[lang][page];
    }

    // Dispatch event so renderers (blog, resources, events) can re-render
    document.dispatchEvent(new CustomEvent('languageChanged'));
}
window.setLanguage = setLanguage;

// ==================== INITIALIZE ====================
document.addEventListener('DOMContentLoaded', function () {
    setLanguage(window.currentLang);

    document.querySelectorAll('.lang-btn').forEach(function (btn) {
        btn.addEventListener('click', function () {
            setLanguage(btn.getAttribute('data-lang'));
        });
    });
});
